export interface Classroom {
    id: number;
    uuid: string;
    name: string;
    created_at?: string;
    updated_at?: string;
    extra_attributes?: Record<string, any>;
}

export interface ClassroomFormData extends Omit<Classroom, 'id' | 'created_at' | 'updated_at' | 'extra_attributes'> {
    id?: number;
    photo?: File | null;
    extra_attributes?: Record<string, any>;
}

export interface ClassroomResponse {
    data: Classroom | Classroom[];
    message?: string;
    success: boolean;
}